#include <unistd.h>

int	view_result(char solution[4][4])
{
	int		itr[2];
	char	c;

	itr[0] = 0;
	while (itr[0] < 4)
	{
		itr[1] = 0;
		while (itr[1] < 4)
		{
			c = solution[itr[0]][itr[1]] + '0';
			write(1, &c, 1);
			if (itr[1] != 3)
				write(1, " ", 1);
			itr[1]++;
		}
		write(1, "\n", 1);
		itr[0]++;
	}
	return (0);
}
